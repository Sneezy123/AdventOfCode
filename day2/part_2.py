total_score = 0


def calculate_score(opponent_move: str, round_ends_with: str, your_score=total_score) -> int:
    if opponent_move == "A" and round_ends_with == "X":  # Rock -> Loss
        your_score += 3  # Loss
    elif opponent_move == "B" and round_ends_with == "X":  # Paper -> Loss
        your_score += 1  # Loss
    elif opponent_move == "C" and round_ends_with == "X":  # Scissors -> Loss
        your_score += 2  # Loss
    elif opponent_move == "A" and round_ends_with == "Y":  # Rock -> Draw
        your_score += 4  # Draw
    elif opponent_move == "B" and round_ends_with == "Y":  # Paper -> Draw
        your_score += 5  # Draw
    elif opponent_move == "C" and round_ends_with == "Y":  # Scissors -> Draw
        your_score += 6  # Draw
    elif opponent_move == "A" and round_ends_with == "Z":  # Rock -> Win
        your_score += 8  # Win
    elif opponent_move == "B" and round_ends_with == "Z":  # Paper -> Win
        your_score += 9  # Win
    elif opponent_move == "C" and round_ends_with == "Z":  # Scissors -> Win
        your_score += 7  # Win

    return your_score


